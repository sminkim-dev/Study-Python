def num_sum(x, y):
    sum = x + y
    print(f"{x} + {y} = {sum}")
def num_sub(x,y):
    sub = x - y
    print(f"{x} - {y} = {sub}")
def num_pow(x , y):
    np = x * y
    print(f"{x} x {y} = {np}")
def num_di(x , y):
    di = x / y
    print(f"{x} / {y} = {di}")

x = 20
y = 10
num_sum(x , y)
num_sub(x , y)
num_pow(x , y)
num_di(x , y)
